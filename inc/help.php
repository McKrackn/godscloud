        <b>Wie registriert man sich?</b>
        <div class="container">
            <p>1. Klicken Sie im Menü auf "Registrieren".</p>
            <p>2. Geben Sie Ihre Kontaktdaten an.</p>
            <p>3. Nach Absenden Ihrer Daten erhalten Sie eine Nachricht darüber ob die Registrierung erfolgreich war.</p>
        </div>
        <b>Wie meldet man sich an?</b>
        <div class="container">
            <p>1. Klicken Sie auf "LogIn".</p>
            <p>2. Geben Sie Ihre Logindaten ein.</p>
            <p>3. Klicken Sie auf Abschicken und Sie werden angemeldet.</p>
        </div>
        <b>Wie kann man sein Passwort oder andere Daten ändern?</b>
        <div class="container">
            <p>1. Melden Sie sich zunächst an.</p>
            <p>2. Klicken Sie auf "Profil".</p>
            <p>3. Dort können Sie Ihr Passwort ändern, ebenso andere Daten ausgenommen E-Mail und Benutzername.</p>
            <p>4. Geben Sie Ihr altes Passwort und die aktualisierten Daten ein.</p>
            <p>5. Klicken Sie auf Abschicken und Ihre Änderungen werden übernommen.</p>
        </div>
        <b>Was macht man wenn man das Passwort vergessen hat?</b>
        <div class="container">
            <p>1. Navigieren Sie zum Loginfenster.</p>
            <p>2. Klicken Sie rechts unter dem Feld zur Passworteingabe auf "Passwort vergessen?".</p>
            <p>3. Geben Sie im folgenden Fenster entweder den Benutzernamen oder die E-Mail-Adresse ein.</p>
            <p>3. Wurde eine entsprechung gefunden, wird automatisiert ein neues Passwort erstellt und an Ihre Mailadresse versandt.</p>
        </div>
        <b>Wie lädt man Bilder hoch?</b>
        <div class="container">
            <p>1. Melden Sie sich zunächst an.</p>
            <p>2. Klicken Sie auf "Bilder".</p>
            <p>3. Dort können Sie mit "Durchsuchen" ein Bild von Ihrer Festplatte aussuchen und hochladen.</p>
        </div>
